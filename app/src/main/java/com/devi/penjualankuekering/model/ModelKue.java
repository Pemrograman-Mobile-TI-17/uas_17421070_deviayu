package com.devi.penjualankuekering.model;

public class ModelKue {

    String _id, kodeKue, namaKue, hargaKue, gambar;

    public String get_id() {
        return _id;
    }

    public void set_id(String _id) {
        this._id = _id;
    }

    public String getKodeKue() {
        return kodeKue;
    }

    public void setKodeKue(String kodeKue) {
        this.kodeKue = kodeKue;
    }

    public String getNamaKue() {
        return namaKue;
    }

    public void setNamaKue(String namaKue) {
        this.namaKue = namaKue;
    }

    public String getHargaKue() {
        return hargaKue;
    }

    public void setHargaKue(String hargaKue) {
        this.hargaKue = hargaKue;
    }

    public String getGambar() {
        return gambar;
    }

    public void setGambar(String gambar) {
        this.gambar = gambar;
    }
}
